package comm.example;

public class ColorFactory {
	private COLOR color;

	public ColorFactory() {
		super();
	}

	public ColorFactory(COLOR color) {
		super();
		this.color = color;
	}

	public COLOR getColor() {
		return color;
	}

	public void setColor(COLOR color) {
		this.color = color;
	}
	
	

}
